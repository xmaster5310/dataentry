    private void table1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table1MouseClicked
        // TODO add your handling code here:
        try {
            int i = table1.getSelectedRow();
            dm = (DefaultTableModel) table1.getModel();
            jLabel10.setText(dm.getValueAt(i, 0).toString());

            jComboBox1.setSelectedItem(dm.getValueAt(i, 1).toString());

            jTextArea1.setText(dm.getValueAt(i, 2).toString());
            jTextField1.setText(dm.getValueAt(i, 3).toString());
            jTextField2.setText(dm.getValueAt(i, 4).toString());

            if (dm.getValueAt(i, 5) == null) {
                jDateChooser2.setDate(null);
            } else {
                java.util.Date date1 = new SimpleDateFormat("yyyy-MM-dd").parse((String) dm.getValueAt(i, 5).toString());
                jDateChooser2.setDate(date1);
            }

        } catch (ParseException ex) {
            Logger.getLogger(Maintainance.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_table1MouseClicked

