    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //validating user input
        if (!validation()) {
            JOptionPane.showMessageDialog(null, "Please fill the form");
            return;
        }
        try {
            String sql = "insert into vban_samuha (name,description,money_taken,money_returned,date) values(?,?,?,?,?)";
            pst = conn.prepareStatement(sql);
            pst.setString(1, (String) jComboBox1.getSelectedItem());
            pst.setString(2, jTextArea1.getText());
//            pst.setString(3, jTextField1.getText());

            if (jTextField1.getText().equals("")) {
                pst.setString(3, "0");
            } else {
                pst.setString(3, jTextField1.getText());
            }

            if (jTextField2.getText().equals("")) {
                pst.setString(4, "0");
            } else {
                pst.setString(4, jTextField2.getText());
            }

            java.util.Date utilDate1 = jDateChooser2.getDate();
            java.sql.Date sqlDate1 = new java.sql.Date(utilDate1.getTime());
            pst.setDate(5, sqlDate1);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Saved");
            jTextArea1.setText("");
            jTextField1.setText("");
            jTextField2.setText("");
            jDateChooser2.setDate(null);
            fetch();
            getSum();
            getSumReturned();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

