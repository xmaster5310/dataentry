    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (!validation()) {
            JOptionPane.showMessageDialog(null, "Please fill the form");
            return;
        }
        try {
            String sql = "insert into sales (type,cubic_ft,rate,total,date) values(?,?,?,?,?)";
            pst = conn.prepareStatement(sql);

            if (jTextField1.getText().equals("")) {
                pst.setString(1, (String) jComboBox1.getSelectedItem());
            } else {
                pst.setString(1, jTextField1.getText());
            }
            pst.setString(2, CubicFt.getText());
            pst.setString(3, rate.getText());
            pst.setString(4, total.getText());
            java.util.Date utilDate = date.getDate();
            java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());
            pst.setDate(5, sqlDate);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Saved");
            fetch();
            getSum();
            verification();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

