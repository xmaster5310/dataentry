    public void fetch() {
        try {
            String q = "select id as Sn,name as 'Samuha Name', money_deposit as 'Deposit Money', money_withdraw as 'Withdraw Money', date_withdraw as 'Withdraw Date' ,date_deposit as 'Deposit Date' from samuha ORDER BY id DESC";
            pst = conn.prepareStatement(q);
            rs = pst.executeQuery();
            table1.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error");
        }
    }

    private void filter(String query) {
        dm = (DefaultTableModel) table1.getModel();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(dm);
        table1.setRowSorter(tr);

        tr.setRowFilter(RowFilter.regexFilter(query));
    }

    public int getSum() {
        int rowsCount = table1.getRowCount();
        int sum = 0;
        for (int i = 0; i < rowsCount; i++) {
            sum = sum + Integer.parseInt(table1.getValueAt(i, 2).toString());
        }
        Tsum.setText(Integer.toString(sum));
        return sum;
    }

    public int getWithdraw() {
        int rowsCount = table1.getRowCount();
        int sum = 0;
        for (int i = 0; i < rowsCount; i++) {
            sum = sum + Integer.parseInt(table1.getValueAt(i, 3).toString());
        }
        Twithdraw.setText(Integer.toString(sum));
        return sum;
    }

    public void getRemaining() {
        try {
            double first, second, total;
            first = Double.parseDouble(Tsum.getText());
            second = Double.parseDouble(Twithdraw.getText());
            total = first - second;
            remaining.setText("" + total);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error");
        }
    }

